/**
 * Translate a key for the given language, falling back to English then Turkish
 */
export function t(key: string, lang: LanguageCode): string {
  return (
    translations[lang]?.[key] ??
    translations.en?.[key] ??
    translations.tr?.[key] ??
    key
  );
}

function getStoredLang(): LanguageCode {
  try {
    const stored = localStorage.getItem(LANG_STORAGE_KEY);
    if (stored && LANGUAGES.find((l) => l.code === stored)) {
      return stored as LanguageCode;
    }
  } catch {
    // ignore
  }
  return "tr";
}

/**
 * React hook for reading/writing the active language
 */
export function useLanguage(): {
  lang: LanguageCode;
  setLang: (lang: LanguageCode) => void;
} {
  const [lang, setLangState] = useState<LanguageCode>(getStoredLang);

  function setLang(newLang: LanguageCode) {
    try {
      localStorage.setItem(LANG_STORAGE_KEY, newLang);
    } catch {
      // ignore
    }
    setLangState(newLang);
  }

  return { lang, setLang };
}
